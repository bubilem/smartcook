  <h1>June 5. 2024</h1>
  <p>In the morning our teams met at school. We had a first IT presentation. Czech students presented their progress in
    creating the web application. After that we set off to OSI. It is an international company for meat processing for
    large fast food chains such as McDonald's. We had an opportunity to observe the whole process of preparing meat. The
    tour was led by one of the company's managers.</p>
  <p>As lunchtime approached, we went to the kitchen and baked a few mini pizzas, rolls, doughnuts, and a few other
    dishes. After a light lunch, we went to the Jump Arena, where we all relaxed for an hour. Finally, we returned to
    school to refresh ourselves with a late lunch in the school canteen and set off to explore the city together.</p>
