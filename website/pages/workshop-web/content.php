  <h1>Web workshop</h1>
  <p>We show Polish students how to create a website.</p>
  <p>In an innovative educational exchange, information technology students embarked on a project 
    to teach chefs how to create their own websites. The initiative began with a comprehensive 
    presentation where an instructor introduced the students to the fundamental principles of web application development. 
    Following this introduction, the students, step by step, crafted a webpage centered around the theme of fruit. 
    This hands-on experience not only allowed the students to apply their theoretical knowledge but also fostered a collaborative environment. 
    They worked together with remarkable success, assisting each other and communicating effectively throughout the process. 
    This cross-disciplinary collaboration highlights the importance of technology in various fields and demonstrates the value 
    of peer-to-peer learning.
  </p>
