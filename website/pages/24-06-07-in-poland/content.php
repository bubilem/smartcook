    <h1>June 7. 2024</h1>
    <p>Today's program was divided into two parts. We spent the morning at school in an IT workshop. Czech students gave
        Polish students a short lesson on adding recipes to the database using a form they had created. And then, we
        spent some time together creating possible logo designs.</p>
    <p>In the early afternoon, and despite the somewhat gloomy weather, we went out to grill. After enjoying a sausage,
        we relaxed for a while in the park. Some of us then went paddle boating on the lake, while others went to
        exercise in a nearby park. Everyone was chatting and enjoying themselves.</p>
