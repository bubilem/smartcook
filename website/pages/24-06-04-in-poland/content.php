    <h1>June 4. 2024</h1>

    <p>Early in the morning, we set off for the "Food laboratory and exhibition Centrum Popularyzacji Nauki i Innowacji
        Kortosfera". We were able to explore a variety of exhibits, from how saltwater affects the movement of particles
        by weight, to sound frequencies influencing sand formation, the effect of light on plant growth, the functioning
        of the economy, and the amount of water needed per person per day, healthy nutrition, and much more. There were
        also many fun and educational games. </p>

    <p>After a few hours of exploring and experimenting, we went to the laboratory to observe microorganisms from our
        own samples. We used sophisticated microbiological techniques to dye oral swab samples. Under a microscope, we
        saw oral epithelial cells and the microbes that were occupying them.</p>

    <p>The rest of the day was spent exploring the town of Olsztyn.</p>

