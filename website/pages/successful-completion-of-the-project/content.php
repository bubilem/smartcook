  <h1>Successful completion of the SMART COOK Erasmus+ project</h1>
  <p>Author: David Pokorák, 24. 10. 2024</p>
  <h2>Introduction:</h2>
  <p>The Erasmus+ project called SMART COOK, which in the past school year involved students of information technology
    from VOŠ, SPŠ a SOŠ, Varnsdorf together with students of gastronomy from a partner school in Ostróda, Poland. The
    project focused on the connection between gastronomy and information technology. The main goal of the project was to
    increase the digital literacy of students, sharing good practices in the field of gastronomy and IT and creating a
    modern web application for efficient recipe management. </p>
  <h2>Project progress:</h2>
  <p>The project took place in two stages. First the students from Poland visited Varnsdorf, and then the Czech
    delegation headed to Ostróda. Each stay lasted one week and was filled with professional workshops focused on topics
    such as food styling, programming and database creation. A key activity was the creation of the SmartCook app, which
    will allow users to generate recipes several days in advance to minimise wasted ingredients.
  </p>
  <p>During the meeting in Poland, students also had the opportunity to visit the modern OSI Poland Foodworks plant,
    where they learned about the hamburger production process. There were also cultural activities, such as a visit to
    the old part of Olsztyn or baking traditional Toruń gingerbread.
  </p>
  <h2>Results and benefits:</h2>
  <p>The project has produced a number of practical results, including the creation of an innovative web application
    that will continue to be used. The students gained experience of working in an international team, which contributed
    to their personal and professional development. At the same time, the relationship between the two schools has been
    strengthened, opening up new possibilities for future cooperation in future projects.</p>
  <h2>Reflection and evaluation:</h2>
  <p>"This project was a huge opportunity for us. The cooperation with the Polish students gave us the opportunity to
    exchange experiences and learn new things," said the students from Varnsdorf. The project gave the students valuable
    experience in both fields, which had a positive impact on their personal and professional growth. </p>
  <h2>Conclusion:</h2>
  <p>The SMART COOK project has been successfully completed, but the cooperation between the schools is not over. Thanks
    to this project, a solid foundation for further cooperation has been established. </p>

