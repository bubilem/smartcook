  <h1>Database workshop</h1>
  <p>We show Polish students how to create a database.</p>
  <p>In an innovative educational exchange, information technology students embarked on a unique project to teach chefs 
    the intricacies of database development. The collaboration began with playful experimentation in Excel, 
    where tables and data manipulation were introduced as foundational skills. Gradually, the group transitioned 
    to MySQL Workbench, meticulously constructing a shared database step by step. Upon its completion, 
    the database was not only built but also fully implemented on the MySQL DBMS. The students explored various 
    SQL queries, enhancing their understanding of data retrieval and management. 
    Organized into small groups, the students fostered a supportive environment, offering assistance and sharing 
    knowledge amongst themselves. This peer-to-peer learning approach proved to be an effective method for both 
    teaching and reinforcing the practical applications of database technologies in a culinary context.
  </p>
