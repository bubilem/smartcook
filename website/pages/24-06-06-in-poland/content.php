  <h1>June 6. 2024</h1>
  <p>Today was an excursion to the city of gingerbread - Toruń. It took about 1.5 hours to get here, but it went by very
    quickly.</p>
  <p>We met the other group in the city center and went on a tour of the city. The whole city is dedicated to the
    production of gingerbread. We found many shops that specialize in gingerbread. Everywhere it was possible to buy
    souvenirs reminiscent of gingerbread. </p>
  <p>After about an hour, we gathered in front of "The Museum of Toruń Gingerbread" where we joined an entertaining
    program that showed and allowed us to try gingerbread making. The program was divided into several sections. The
    guides were actors dressed in period costumes. They told us the history of gingerbread production, described its
    differences and finally we had the opportunity to make gingerbread ourselves.</p>
  <p>After the workshop, we went back to the streets of Toruń.</p>

