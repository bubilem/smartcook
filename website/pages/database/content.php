  <h1>Database</h1>
  <p>How we created the database.</p>
  <p>The SmartCook project's database design was a collaborative effort involving several brainstorming sessions with students. 
    The team focused on creating an Entity-Relationship (E-R) schema that would effectively represent the data requirements of the 
    project. After multiple iterations, a final design was achieved that met all the initial requirements. The database is 
    relational and operates on a MySQL Database Management System (DBMS). In pursuit of optimization, certain static tables were 
    replaced with data stored in JSON format, which allows for more flexibility and scalability. This approach not only streamlined 
    the database structure but also enhanced the performance and maintainability of the system, ensuring that SmartCook's database 
    is robust and efficient.
  </p>
