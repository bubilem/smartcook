  <h1>October 19, 2023</h1>
  <p></p>
  <p>Since morning it had been raining heavily, so our day began with a meet-up at school. From school, we went to a local brewery called Kocour, where we were told about their beer and lemonade production. We toured the whole brewery, and the local brewer described the brewing process. 
  Next, we took a bus ride to a local sweet shop called Hosana Coffee. A lady there told us about their history and how they manage their social media sites. It was a little lesson in online marketing. 
  We returned to school for a lunch break. After that, we went to the kitchen to cook. Each group made their own dish consisting of an appetizer, soup, main course, and dessert. The teams competed with each other to see who could prepare the tastiest dish. The judges were our teachers. In the end, one group went to an escape room, while the other headed home. 
  </p>
