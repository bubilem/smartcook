  <h1>October 18, 2023</h1>
  <p></p>
  <p>We set out for Tuchom Farm in the morning, and upon arrival, we were greeted by our guide. 
    After the introduction, we took a walk around the farm, 
    during which the guide explained how the biofarm works and introduced us to the various animals they have. 
    After the walk, we were treated to a delicious lunch of “halušky”. Following that, we were shown how cheese is made, 
    and we even got to try our hand at making some ourselves. Additionally, we were offered a variety of cheeses to taste. 
  </p>
