  <p>As part of the Erasmus+ project we went to Poland as a group of Czech students. This programme, supported by the
    European Union, allowed us to gain experience abroad. The aim of this visit was not only to study, but also to have
    a cultural exchange and make new friends.</p>
  <p>The project included lectures, presentations, workshops, visits to information and sports centres and international
    companies operating in the gastronomy industry. It brought us not only practice in the information and culinary
    fields, but also unforgettable memories. The main outcome of the project is the creation of a web application called
    SmartCook, which will serve as a smart cookbook and allow users to view recipes, compose a menu and then create a
    shopping list from that.</p>
  <p>We would like to thank the Erasmus+ project very much for giving us the opportunity to participate in the project
    and gain experience for a lifetime.</p>
