  <h1>Logo</h1>
  <p>How we created a logo for our project</p>
  <p>The creative process for designing the logo for the SmartCook cookbook was a journey of experimentation and discovery. 
    The team explored various concepts, experimenting with different shapes and designs that could encapsulate the essence 
    of a smart culinary guide. After numerous iterations, the winning design emerged: a charming cookie emblazoned with the 
    SmartCook name, symbolizing both the smartness and the cooking theme of the product. The final touch was selecting the 
    perfect font to complement the logo's friendly and approachable vibe. After much deliberation, the choice fell on 
    <a href="https://fonts.google.com/specimen/Gluten" title="Gluten Font Designed by Tyler Finck, ETC">Gluten from Google Fonts</a>, 
    known for its clean and modern feel, which aligned seamlessly with the brand's identity. This meticulous process not only resulted in a 
    visually appealing logo but also one that resonates with the innovative spirit of SmartCook.
  </p>
  <p>We used <a href="https://inkscape.org/">Inkscape</a> to create the logo.</p>
