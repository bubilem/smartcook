  <h1>October 20, 2023</h1>
  <p></p>
  <p>On Friday morning, we set off to Prague. 
    Upon arrival, we divided into two groups consisting of four Czech and four Polish students. 
    In the morning, we enjoyed a tour of the Chocolate Museum, where we had the opportunity to taste and decorate our own chocolate. 
    Later, we showed our Polish friends the most famous sights of Prague, 
    and finally we took part in a photography course led by a professional photographer. 
    Together, we experimented with capturing intriguing still-life photos of food and had a delightful time before returning home. 
