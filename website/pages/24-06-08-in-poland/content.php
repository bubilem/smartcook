    <h1>June 8. 2024</h1>
    <p>In the morning we went to the last workshop in the village of Lubajny, where we visited the local cultural
        centre. They welcomed us warmly. Together we cooked soup and pancakes according to a local recipe. We were
        educated about the local history and traditions.</p>
    <p>Before lunch, we went for a walk together to the forest where we had other joint sports activities. On the walk
        we stopped at the church and had a chance to talk to the pastor. On our return we served lunch which was already
        prepared.</p>
    <p>In the afternoon we roasted sausages and sausages and slowly said goodbye to each other. Nobody wanted to go
        home.</p>
